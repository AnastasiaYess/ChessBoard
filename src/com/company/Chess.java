package com.company;

import java.util.Scanner;

public class Chess {

    int length;
    int height;

    public Chess(int length, int height) {
        this.length = length;
        this.height = height;
    }

    public int getLength(int length) {
        return this.length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }




}


